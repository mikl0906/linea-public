// Steel Beam Block — elevation (front/top view) of a catalogue profile
// between two picked points: solid outer edges, dashed hidden edges, end
// caps. Catalogue data from the calculus profile catalogs. The defineBlock
// call sits at the END of the file, below the data tables it reads.
import { defineBlock } from "lineadraw";
import type { Line, Vec2 } from "lineadraw";
import { sub, add, scale, norm, rotate, len } from "lineadraw/helpers";

export default defineBlock({
  id: "fa9c5e7e-4629-40ee-9eed-313495c10743",
  name: "Steel Beam",
  description: "Draws a beam from a catalogue profile between two picked points.",
  version: "1.0.0",
  authors: ["Linea Team"],
  tags: ["structural", "steel", "catalog", "building"],
  params: () => [
    {
      name: "catalog",
      label: "Catalog",
      type: "enum",
      default: "IPE",
      options: [
        "IPE",
        "HEA",
        "HEB",
        "UPE",
        "UPN",
        "L Equal",
        "L Unequal",
        "CFCHS",
        "CFSHS",
        "CFRHS",
      ],
    },
    { name: "ipe", label: "Profile", type: "enum", default: "IPE300", options: ipes.map((p) => p[0]) },
    { name: "hea", label: "Profile", type: "enum", default: "HEA300", options: heas.map((p) => p[0]) },
    { name: "heb", label: "Profile", type: "enum", default: "HEB300", options: hebs.map((p) => p[0]) },
    { name: "upe", label: "Profile", type: "enum", default: "UPE200", options: upes.map((p) => p[0]) },
    { name: "upn", label: "Profile", type: "enum", default: "UPN200", options: upns.map((p) => p[0]) },
    { name: "lequal", label: "Profile", type: "enum", default: "L100x10", options: lequals.map((p) => p[0]) },
    { name: "lunequal", label: "Profile", type: "enum", default: "L150x100x10", options: lunequals.map((p) => p[0]) },
    { name: "cfchs", label: "Profile", type: "enum", default: "CFCHS168.3x5", options: cfchss.map((p) => p[0]) },
    { name: "cfshs", label: "Profile", type: "enum", default: "CFSHS150x5", options: cfshss.map((p) => p[0]) },
    { name: "cfrhs", label: "Profile", type: "enum", default: "CFRHS200x100x5", options: cfrhss.map((p) => p[0]) },
    {
      name: "orientation",
      label: "Orientation",
      type: "enum",
      default: "front",
      options: [
        { value: "front", label: "Front" },
        { value: "top", label: "Top" },
      ],
    },
  ],
  paramVisibility: ({ params }) => ({
    ipe: params.catalog === "IPE",
    hea: params.catalog === "HEA",
    heb: params.catalog === "HEB",
    upe: params.catalog === "UPE",
    upn: params.catalog === "UPN",
    lequal: params.catalog === "L Equal",
    lunequal: params.catalog === "L Unequal",
    cfchs: params.catalog === "CFCHS",
    cfshs: params.catalog === "CFSHS",
    cfrhs: params.catalog === "CFRHS",
  }),
  place: ["Start point", "End point"],
  draw: ({ params, inputs: [p1, p2] }) => {
    const section = ((): Section | null => {
      switch (params.catalog) {
        case "IPE":
        case "HEA":
        case "HEB": {
          const table =
            params.catalog === "IPE"
              ? ipes
              : params.catalog === "HEA"
                ? heas
                : hebs;
          const name =
            params.catalog === "IPE"
              ? params.ipe
              : params.catalog === "HEA"
                ? params.hea
                : params.heb;
          const p = table.find((p) => p[0] === name);
          return p ? { kind: "I", h: p[1], w: p[2], tw: p[3], tf: p[4] } : null;
        }
        case "UPE":
        case "UPN": {
          const table = params.catalog === "UPE" ? upes : upns;
          const name = params.catalog === "UPE" ? params.upe : params.upn;
          const p = table.find((p) => p[0] === name);
          return p ? { kind: "U", h: p[1], w: p[2], tw: p[3], tf: p[4] } : null;
        }
        case "L Equal":
        case "L Unequal": {
          const table = params.catalog === "L Equal" ? lequals : lunequals;
          const name =
            params.catalog === "L Equal" ? params.lequal : params.lunequal;
          const p = table.find((p) => p[0] === name);
          return p ? { kind: "L", h: p[1], w: p[2], t: p[3] } : null;
        }
        case "CFCHS": {
          const p = cfchss.find((p) => p[0] === params.cfchs);
          return p ? { kind: "CHS", d: p[1], t: p[2] } : null;
        }
        case "CFSHS": {
          const p = cfshss.find((p) => p[0] === params.cfshs);
          return p ? { kind: "RHS", h: p[1], w: p[1], t: p[2] } : null;
        }
        case "CFRHS": {
          const p = cfrhss.find((p) => p[0] === params.cfrhs);
          return p ? { kind: "RHS", h: p[1], w: p[2], t: p[3] } : null;
        }
      }
      return null;
    })();

    const dir = sub(p2, p1);
    if (!section || len(dir) < 1e-9) return [];
    const n = norm(rotate(dir, Math.PI / 2));

    // Signed offsets across the beam axis. Front looks at the section's h
    // extent, top at its w extent; asymmetric sections (U web, L legs) sit
    // on the +n side.
    const solid: number[] = [];
    const hidden: number[] = [];
    const front = params.orientation === "front";
    const s = section;
    if (s.kind === "I" || s.kind === "U") {
      if (front) {
        solid.push(s.h / 2, -s.h / 2, s.h / 2 - s.tf, -s.h / 2 + s.tf);
      } else if (s.kind === "I") {
        solid.push(s.w / 2, -s.w / 2);
        hidden.push(s.tw / 2, -s.tw / 2);
      } else {
        solid.push(s.w / 2, -s.w / 2);
        hidden.push(s.w / 2 - s.tw);
      }
    } else if (s.kind === "L") {
      if (front) solid.push(s.h / 2, -s.h / 2, -s.h / 2 + s.t);
      else solid.push(s.w / 2, -s.w / 2, s.w / 2 - s.t);
    } else if (s.kind === "CHS") {
      solid.push(s.d / 2, -s.d / 2);
      hidden.push(s.d / 2 - s.t, -s.d / 2 + s.t);
    } else if (s.kind === "RHS") {
      const across = front ? s.h : s.w;
      solid.push(across / 2, -across / 2);
      hidden.push(across / 2 - s.t, -across / 2 + s.t);
    }

    const at = (o: number): Vec2 => scale(n, o);
    const lines: Line[] = [];
    // End caps across the full outer extent.
    const hi = Math.max(...solid);
    const lo = Math.min(...solid);
    lines.push({ type: "line", a: at(hi), b: at(lo) });
    lines.push({ type: "line", a: add(dir, at(hi)), b: add(dir, at(lo)) });
    for (const o of solid)
      lines.push({ type: "line", a: at(o), b: add(dir, at(o)) });
    for (const o of hidden)
      lines.push({
        type: "line",
        lineType: "hidden",
        a: at(o),
        b: add(dir, at(o)),
      });
    return lines;
  },
});

